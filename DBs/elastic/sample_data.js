const getRandomElement = (array) =>
  array[Math.floor(Math.random() * array.length)];

const getRandomInt = (min, max) =>
  Math.floor(Math.random() * (max - min + 1)) + min;

export const generateRandomParagraph = () => {
  const paragraph = [];
  const sentenceCount = getRandomInt(4, 10);

  for (let i = 0; i < sentenceCount; i++) {
    const subject = getRandomElement(subjects);
    const verb = getRandomElement(academicVerbs);
    const noun = getRandomElement(fancyNouns);
    const adj = getRandomElement(adjectives);

    const sentence =
      `В рамках данной лекции ${verb} ${subject}, где особое внимание уделяется такому явлению, как ${adj} ${noun}. ` +
      `Этот подход позволяет расширить представление о теме за счёт включения междисциплинарных связей и критического анализа.`;

    paragraph.push(sentence);
  }

  return paragraph.join(" ");
};
